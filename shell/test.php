function uuid($prefix=null)
{
    return strtolower(md5(uniqid($prefix.md5(mt_rand()),true)));
}

function md5_uuid($key,$prefix=null)
{
    return strtolower(md5(hash_hmac("sha256",md5(uniqid($prefix.md5(mt_rand()),true)),$key)));
}
